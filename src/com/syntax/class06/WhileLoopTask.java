package com.syntax.class06;

public class WhileLoopTask {

	public static void main(String[] args) {
		int i=0;
		while(i<=10) {
		System.out.print(i+" ");
		i+=2;
		}
		
		System.out.println("Outside of the loop");
		System.out.println("Another way");
		int a=1;
		while (a<=10) {
			if (a%2==0) {
				System.out.print(a+" ");
		}
			a++;
	}

}
}