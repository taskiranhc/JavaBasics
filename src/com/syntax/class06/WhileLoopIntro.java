package com.syntax.class06;

public class WhileLoopIntro {

	public static void main(String[] args) {
		int time = 14;
		if (time < 17) {
			System.out.println("It is a day");
		}
		while (time < 17) {
			System.out.println("It is a day");
			time++;
		}
		System.out.println("-------------------");
	

	
	int i=1;
	while(i<=20) {
	System.out.print(i+" ");
	i++;
	}
	System.out.println(" ");
	System.out.println("-------------------");
	
	int y=50;
	while(y>=1) {
		System.out.print(y+" ");
		y--;
	}
		System.out.println(" ");
		System.out.println("End of code");
	}
}

