package com.syntax.homeWorks;

public class LoopHw {

	public static void main(String[] args) {
		int i=1;
		while (i<=100) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println(" ");
		System.out.println("---------------------------------- ");
		int a=100;
		while (a>=1) {
			System.out.print(a+" ");
			a--;
		}
		System.out.println(" ");
		System.out.println("---------------------------------- ");
		int b=20;
		while (b<=100) {
			System.out.print(b+" ");
			b+=2;
		}
		System.out.println(" ");
		System.out.println("---------------------------------- ");
		int c=99;
		while (c>=1) {
			System.out.print(c+" ");
			c-=2;
		}
		System.out.println(" ");
		System.out.println("END OF CODE ");
	}

}
