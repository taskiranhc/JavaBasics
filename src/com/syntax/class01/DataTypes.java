package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {

		// representing and storing whole numbers

		byte box1 = 127; // range from -128 till 127
		short box2 = 32767; // range from -32768 till 32767
		int box3 = 100000; // most popular data type for whole number
		long box4 = 999999100000000L; // to specify long numbers use the letter l or L at the end

		// representing and storing decimal numbers

		float a = 1.99f;		
		double b = 19.99; // most popular data type to use for decimal numbers
		
		// representing and storing single character
		
		char character='#';
		char letter='c';
		
		// representing and storing boolean values (yes/no)
		
		boolean hungry=true; //yes
		boolean tired=false; //no
		
		System.out.println(box1);
		System.out.println("box1");
		System.out.println(a);
		
		
		
		

	}

}
