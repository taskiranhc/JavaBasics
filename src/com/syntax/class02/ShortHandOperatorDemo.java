package com.syntax.class02;

public class ShortHandOperatorDemo {

	public static void main(String[] args) {
	int nmbr=10;
	nmbr=nmbr+1;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr++;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr=10;
	nmbr=nmbr+30;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr+=30;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr=10;
	nmbr-=5;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr=10;
	nmbr*=5;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr=10;
	nmbr/=5;
	System.out.println(nmbr);
	System.out.println("***************************************");
	nmbr=10;
	nmbr%=5;
	System.out.println(nmbr);
	
	}

}
